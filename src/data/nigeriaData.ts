/**
 * Generate normalized LGA array from the map
 * Each LGA object contains the LGA name and its state code
 */
function generateLGAs() {
  const lgas: { name: string; state: string }[] = [];
  
  for (const [stateCode, lgaNames] of Object.entries(LGA_MAP)) {
    for (const lgaName of lgaNames) {
      lgas.push({ name: lgaName, state: stateCode });
    }
  }
  
  return lgas;
}

/**
 * All LGAs in Nigeria (774 total)
 * Each LGA contains name and state code reference
 */
